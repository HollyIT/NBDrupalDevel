    private void setVersions() {
        ArrayList items = WizardUtils.getMajorVersions("module");
        String drVer = NbPreferences.forModule(DrupalDevelTool.class).get("drupalVersion", "");
        this.cbDrupalVersion.removeAllItems();
        for (int i = 0; i < items.size(); i++) {
            String item = items.get(i).toString();
            Object obj = makeObj(item);
            cbDrupalVersion.addItem(obj);
            if (obj.toString().equals(drVer)) {
                cbDrupalVersion.setSelectedItem(obj);
            }
        }
    }

    private Object makeObj(final String item) {
        return new Object() {

            @Override
            public String toString() {
                return item;
            }
        };
    }

